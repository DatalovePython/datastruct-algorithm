package leetcode;

public class LengthOfLongestSubstring {
    public int lengthOfLongestSubstring(String s){
        if(s.length()==0 || s.length()==1){
            return s.length();
        }else{
            for(int i=0;i<s.length();i++){
                return 1;
            }
        }
        return 1;
    }
}
